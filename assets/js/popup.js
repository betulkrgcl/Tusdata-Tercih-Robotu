function showPopup() {
    const popup = document.querySelector('.popup');
    popup.style.display = "flex";
}

function hidePopup() {
    const popup = document.querySelector('.popup');
    popup.style.display = "none";
}

function expandPopup() {
    const popup = document.querySelector('.expand');
    popup.style.display = "flex";
}

function hideExpand() {
    const hideExpand = document.querySelector('.expand');
    hideExpand.style.display = "none";
}